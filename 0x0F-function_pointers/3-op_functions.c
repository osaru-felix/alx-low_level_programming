#include <stdio.h>

/**
 * op_add - Function returns the sum of two values
 * @a: value 1
 * @b: value 2
 * Return: sum
 */

int op_add(int a, int b)
{
	return (a + b);
}


/**
 * op_sub - Function returns the difference of two values
 * @a: value 1
 * @b: value 2
 * Return: Difference
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - Function returns the product of two values
 * @a: value 1
 * @b: Value 2
 * Return: Product
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - Function returns the quotient of two values
 * @a: value 1
 * @b: value 2
 * Return: Quotient
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - Function returns the remainder of two values
 * @a: value 1
 * @b: value 2
 * Return: Remainder
 */

int op_mod(int a, int b)
{
	return (a % b);
}
